							<h1>Jurassic Park</h1>
							<p>&nbsp;&nbsp;&nbsp;&nbsp;Jurassic Park is an American science fiction media franchise centered on a disastrous attempt to create a theme park of cloned dinosaurs. It began in 1990 when Universal Pictures and Amblin Entertainment bought the rights to the novel by Michael Crichton before it was even published.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							The book was successful, as was Steven Spielberg's 1993 film adaptation, which led to four sequels, although only the first two films were based on the novels. Numerous video games based on the franchise have been created by various software developers since the release of the 1993 film. Since 1996, several water rides based on the series have been opened at various Universal theme parks.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							The Jurassic Park Ultimate Trilogy was released on DVD and Blu-ray on October 25, 2011, in North America. The first film was re-released in 3D on April 5, 2013.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							The fourth film, Jurassic World, was initially scheduled to be released in the summer of 2005, but was delayed numerous times and was ultimately released in June 2015. It became the first film to gross over $500 million worldwide in its opening weekend, and grossed over $1.6 billion through the course of its theatrical run, making it the sixth highest-grossing film of all time. It was also the second highest-grossing film of 2015. When adjusted for monetary inflation, this film is the second highest grossing in the franchise after Jurassic Park. An animated film, Lego Jurassic World: The Indominus Escape, was released in October 2016 with the home media release of Jurassic World, alongside a short film, Employee Safety Video. Two other interactive Lego short films, Lego Jurassic World: Rescue Blue/Escape the Indoraptor, were also released in 2018, followed by an animated special, Lego Jurassic World: The Secret Exhibit, which aired on NBC later that year.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							A fifth film, Jurassic World: Fallen Kingdom, was released in June 2018. The film grossed over $1.3 billion worldwide, making it the third Jurassic film to pass the billion dollar mark. It is the third highest-grossing film of 2018 and the 12th highest-grossing film of all time. A sixth film, tentatively titled Jurassic World 3, is scheduled to be released on June 11, 2021. As of 2000, the franchise had generated $5 billion in revenue, making it one of the highest-grossing media franchises of all time.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							In 2018, the first film was selected for preservation in the United States National Film Registry by the Library of Congress as being "culturally, historically, or aesthetically significant."</p>
