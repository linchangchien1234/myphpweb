							<h1>Good Dinosaur</h1>
							<p>&nbsp;&nbsp;&nbsp;&nbsp;The Good Dinosaur is a 2015 American 3D computer-animated drama adventure film produced by Pixar Animation Studios and released by Walt Disney Pictures. Directed by Peter Sohn in his directorial debut and written by Meg LeFauve from an original idea by Bob Peterson, the film is set in an alternate history in which dinosaurs never became extinct. The film follows a young and timid Apatosaurus named Arlo, who meets an unlikely human friend while traveling through a harsh and mysterious landscape. The film features the voices of Raymond Ochoa, Jack Bright, Sam Elliott, Anna Paquin, A.J. Buckley, Steve Zahn, Jeffrey Wright and Frances McDormand.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							Peterson, who came up with the idea for the story, directed the film until August 2013. In October 2014, Sohn was announced as the new director. The film, alongside Inside Out, marked the first time Pixar released two feature films in the same year.
							<br>&nbsp;&nbsp;&nbsp;&nbsp;
							The Good Dinosaur premiered on November 10, 2015 in Paris and was released in the United States on November 25, 2015. The film grossed $332 million worldwide against a $175 million budget. This resulted in a total net loss of $85 million, becoming the first Pixar film to be a box office bomb.</p>
